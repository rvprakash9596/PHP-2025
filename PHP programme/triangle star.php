<?php
$k=10;
for($i=1; $i<=5; $i++)
{
	for($m=1; $m<=$k; $m++)
		echo("&nbsp");
		$k--;
		for($j=1; $j<=$i; $j++)
		{
			echo("*&nbsp");
		}
		echo"<br>";
		
}

?>
